"""Lista 05-Operadores - Exercicio 8.

Ler dois números inteiros, x e y, e imprimir o quociente e o resto da divisão inteira entre eles.
"""

x = int(input("Valor de x..: "))
y = int(input("Valor de y..: "))

quociente = x / y
resto = x % y

print("Quociente...: ", quociente)
print("Resto.......: ", resto)
